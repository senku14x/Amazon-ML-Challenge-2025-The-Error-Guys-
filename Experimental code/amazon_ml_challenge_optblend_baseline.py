def robust_read_csv(path: Path, encodings=("utf-8","latin-1","iso-8859-1","cp1252")) -> pd.DataFrame:
    for enc in encodings:
        try:
            return pd.read_csv(path, encoding=enc)
        except Exception:
            continue
    raise RuntimeError(f"Could not read {path}")

